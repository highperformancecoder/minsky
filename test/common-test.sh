# common setup code for regression tests
if test $? -ne 0; then exit 2; fi
tmp=/tmp/$$
mkdir $tmp
if test $? -ne 0; then exit 2; fi
cd $tmp
if test $? -ne 0; then exit 2; fi

fail()
{
    echo "FAILED" 1>&2
    cd $here
    chmod -R u+w $tmp
    rm -rf $tmp
    exit 1
}

pass()
{
    echo "PASSED" 1>&2
    cd $here
    chmod -R u+w $tmp
    rm -rf $tmp
    exit 0
}

trap "fail" 1 2 3 15

# add here to PYTHONPATH to find local build of pyminsky
export PYTHONPATH=$here:$PYTHONPATH
export LD_LIBRARY_PATH=$here/ecolab/lib:$LD_LIBRARY_PATH
